# The aim of this project is to investigate whether the occurrence of Castor fiber may influence the occurrence of Libellula depressa in Switzerland.

# Beaver populations have strongly expanded in Switzerland during the last decades. 
# Beavers will construct dams and this will completely change the shap of the sourrunding habitat. 
# This will create a bigger habitat heterogeneity and slow the watter by creating ponds and wetland. 
# Thus, their presence may provide suitable conditions for many aquatic organisms.

# Libellula depressa is a dragonfly species often associated with recently created ponds and open freshwater habitats. 
# Therefore, beaver presence could potentially favour the establishment of this species.

# The main hypothesis of this project is that Libellula depressa occurrences should be found more frequently close to Castor fiber occurrences. 
# Thus, the probability of observing Libellula depressa should increase with a decreasing distance from beaver observation. 

# The environmental matrix created during Part 1 of the project will be used as the starting dataset. 
# To address the ecological question, a new variable representing the distance to the nearest Castor fiber occurrence will be created and added to the matrix.

# The analyses will then compare the environmental niches of both
# species and evaluate the importance of environmental variables
# and beaver proximity using machine learning approaches.